package org.spring.by.bean;

/**
 * @author by@Deng
 * @create 2020-04-04 16:58
 */
public class Student {

    private int age;
    private String address;
    private int grade;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }
}
